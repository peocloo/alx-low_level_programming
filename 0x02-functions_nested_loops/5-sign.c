#include "main.h"
/**
 *@n:int argument for function
 *Return: integer
 *print_sign- function that prints sign of number.
 */

int print_sign(int n)
{
	if (n > 0)
	{
		_putchar('+');
		return (1);
	}
	else if (n == 0)
	{
		_putchar('0');
		return (0);
	}
	else
	{
		_putchar('-');
		return (-1);
	}
}
	
